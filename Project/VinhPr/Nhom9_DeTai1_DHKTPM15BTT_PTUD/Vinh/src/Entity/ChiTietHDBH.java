package Entity;

import java.math.BigDecimal;

public class ChiTietHDBH {
	private HoaDonBanHang maHD;
	private SanPham maSP;
	private int soL;
	private BigDecimal tT;

	public ChiTietHDBH() {
		super();
	}

	public ChiTietHDBH(HoaDonBanHang maHD, SanPham maSP, int soL, BigDecimal tT) {
		super();
		this.maHD = maHD;
		this.maSP = maSP;
		this.soL = soL;
		this.tT = tT;
	}

	public HoaDonBanHang getMaHD() {
		return maHD;
	}

	public void setMaHD(HoaDonBanHang maHD) {
		this.maHD = maHD;
	}

	public SanPham getMaSP() {
		return maSP;
	}

	public void setMaSP(SanPham maSP) {
		this.maSP = maSP;
	}

	public int getSoL() {
		return soL;
	}

	public void setSoL(int soL) {
		this.soL = soL;
	}

	public BigDecimal gettT() {
		return tT;
	}

	public void settT(BigDecimal tT) {
		this.tT = tT;
	}

	@Override
	public String toString() {
		return "ChiTietHDBH [maHD=" + maHD + ", maSP=" + maSP + ", soL=" + soL + ", tT=" + tT + "]";
	}

}
